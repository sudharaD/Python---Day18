class Challenge1:

    from turtle import Turtle as t

    lenny_the_turtle = t()
    lenny_the_turtle.shape("turtle")
    lenny_the_turtle.color("red", "green")

    def square(self, width, height):
        self.lenny_the_turtle.forward(width)
        self.lenny_the_turtle.right(90)
        self.lenny_the_turtle.forward(height)
        self.lenny_the_turtle.right(90)
        self.lenny_the_turtle.forward(width)
        self.lenny_the_turtle.right(90)
        self.lenny_the_turtle.forward(height)
        self.lenny_the_turtle.right(90)





